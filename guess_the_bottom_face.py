# Guess the bottom face (https://www.codechef.com/problems/BOTTOM)

"""
It is known that in regular dice, the sum of opposite faces is 7.
A regular dice is rolled and you are given the value X showing on the top face. Determine the value on
the bottom face.
"""


for _ in range(int(input())):
    print(7 - int(input()))
